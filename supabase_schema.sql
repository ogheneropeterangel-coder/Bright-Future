-- 4. HELPER FUNCTIONS
CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS boolean SECURITY DEFINER SET search_path = public, auth AS $$
BEGIN
  RETURN (auth.jwt() -> 'user_metadata' ->> 'role') = 'admin' OR 
         EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role::text = 'admin');
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION public.is_staff()
RETURNS boolean SECURITY DEFINER SET search_path = public, auth AS $$
BEGIN
  RETURN (auth.jwt() -> 'user_metadata' ->> 'role') IN ('admin', 'teacher', 'cashier', 'exam_officer') OR 
         EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role::text IN ('admin', 'teacher', 'cashier', 'exam_officer'));
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION public.is_cashier()
RETURNS boolean SECURITY DEFINER SET search_path = public, auth AS $$
BEGIN
  RETURN (auth.jwt() -> 'user_metadata' ->> 'role') = 'cashier' OR 
         EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role::text = 'cashier');
END;
$$ LANGUAGE plpgsql;

